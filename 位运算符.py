print(bin(3))  # 3 -> 011
print(bin(4))  # 4 -> 100
print(3 | 4)  # 3 & 4 -> 111  对应十进制-> 7
print("*" * 10)
# ===================================================================
print(bin(2))  # 2 -> 010
print(bin(3))  # 3 -> 011
print(3 ^ 2)  # 2 ^ 3 -> 001
print("*" * 10)
# ====================================================================
# 由于只有第一位不同所以按位异或的结果就是1
print(bin(3))  # 3 -> 011
print(bin(4))  # 4 -> 100
print(3 ^ 4)  # 3 ^ 4 -> 111 对应十进制-> 7

print(bin(8))  # 1000
print(8 >> 2)  # 1000向右边移动两位就是10，10是是十进制，也就是2
